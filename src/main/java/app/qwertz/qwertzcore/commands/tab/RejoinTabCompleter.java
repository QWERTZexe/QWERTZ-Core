package app.qwertz.qwertzcore.commands.tab;

import org.bukkit.command.Command;
import org.bukkit.command.CommandSender;
import org.bukkit.command.TabCompleter;

import java.util.ArrayList;
import java.util.Arrays;
import java.util.List;

public class RejoinTabCompleter implements TabCompleter {
    @Override
    public List<String> onTabComplete(CommandSender sender, Command command, String alias, String[] args) {
        List<String> completions = new ArrayList<>();

        if (args.length == 1) {
            List<String> subCommands = Arrays.asList("enable", "disable", "time");
            for (String subCommand : subCommands) {
                if (subCommand.toLowerCase().startsWith(args[0].toLowerCase())) {
                    completions.add(subCommand);
                }
            }
        } else if (args.length == 2 && args[0].equalsIgnoreCase("time")) {
            // Suggest common rejoin times
            List<String> commonTimes = Arrays.asList("15", "30", "60", "120", "300");
            for (String time : commonTimes) {
                if (time.startsWith(args[1])) {
                    completions.add(time);
                }
            }
        }

        return completions;
    }
}
